# У нас есть два списка строк
first = ['Strings', 'Student', 'Computers']
second = ['Строка', 'Урбан', 'Компьютер']

# 1. Создаём первую генераторную сборку (first_result):
# Эта генераторная сборка будет вычислять разницу в длинах строк из двух списков,
# если их длины не равны.

# Мы используем функцию zip, которая объединяет два списка в пары. Например:
# zip(['Strings', 'Student', 'Computers'], ['Строка', 'Урбан', 'Компьютер'])
# создаст такие пары: ('Strings', 'Строка'), ('Student', 'Урбан'), ('Computers', 'Компьютер')

# Генераторная сборка работает так:
# - Для каждой пары строк (f, s) из списков first и second:
#   - Сначала проверяется условие: if len(f) != len(s)
#     (Если длины строк f и s не равны).
#   - Если длины разные, то возвращается разница их длин: len(f) - len(s).
# - Если же длины одинаковые, строка пропускается (ничего не возвращается).

first_result = (
    len(f) - len(s)  # Возвращаем разницу в длинах строк
    for f, s in zip(first, second)  # Проходим по спискам с помощью zip
    if len(f) != len(s)  # Проверяем, равны ли длины строк
)

# 2. Создаём вторую генераторную сборку (second_result):
# Эта генераторная сборка будет возвращать True или False в зависимости от того,
# равны ли длины строк на одинаковых позициях в двух списках.

# Здесь мы не используем zip, а перебираем элементы списков по индексам.
# Мы используем range(len(first)), чтобы пройти по индексам списка first (например, 0, 1, 2).
# Внутри генератора для каждого индекса i:
# - Мы сравниваем длины строк first[i] и second[i].
# - Если длины равны, возвращается True, иначе False.

second_result = (
    len(first[i]) == len(second[i])  # Сравниваем длины строк по индексу i
    for i in range(len(first))  # Проходим по индексам от 0 до длины списка first
)

# Теперь выведем результаты на экран.
# Для того чтобы увидеть все результаты сразу, мы преобразуем генераторы в список с помощью list().
# Это нужно, потому что генератор не сохраняет свои значения в памяти — он их создаёт "на лету".

# Выводим список разниц длин (если длины не равны)
print(list(first_result))  # Ожидаемый результат: [1, 2]

# Выводим список результатов сравнения длин (True/False)
print(list(second_result))  # Ожидаемый результат: [False, False, True]
