#Цель: научиться обращаться к функциям, как к объекту и передавать их в другие функции для вызова.


#  Задача "Вызов разом":

# Определяем функцию apply_all_func, которая будет принимать список чисел (int_list)
# и произвольное количество функций (*functions). Звездочка (*) перед именем аргумента
# означает, что в этот параметр можно передать несколько функций через запятую.
def apply_all_func(int_list, *functions):
    # Создаем пустой словарь, в который будем записывать результаты работы каждой функции
    results = {}

    # Теперь нам нужно перебрать каждую функцию, которая была передана в функции (они все
    # хранятся в *functions). Это делается с помощью цикла for.
    for func in functions:
        # Для каждой функции из списка functions мы применяем её к списку int_list.
        # Это означает, что мы передаем список int_list в качестве аргумента функции func.
        # Пример: func может быть max, min, sum и т.д., и она выполнит операцию с нашим списком.

        # Атрибут func.__name__ возвращает строку с именем функции (например, 'max' или 'min').
        # Мы добавляем результат работы функции в словарь results. Ключом будет имя функции,
        # а значением - результат её работы на int_list.
        results[func.__name__] = func(int_list)

    # После того, как все функции применены, мы возвращаем наш словарь results.
    # В нём будут все результаты выполнения каждой функции.
    return results


# Теперь давайте протестируем нашу функцию с несколькими примерами.

# Пример 1: Передаем список [6, 20, 15, 9] и две функции max и min.
# max - это стандартная функция, которая возвращает максимальное число из списка.
# min - это стандартная функция, которая возвращает минимальное число из списка.
# Ожидаемый результат: {'max': 20, 'min': 6}
print(apply_all_func([6, 20, 15, 9], max, min))

# Пример 2: Передаем тот же список, но теперь используем три функции: len, sum и sorted.
# len - возвращает количество элементов в списке.
# sum - возвращает сумму всех элементов в списке.
# sorted - возвращает новый отсортированный список на основе переданного.
# Ожидаемый результат: {'len': 4, 'sum': 50, 'sorted': [6, 9, 15, 20]}
print(apply_all_func([6, 20, 15, 9], len, sum, sorted))

